# ------------ keys() Methods --------------
#
#  This method returns a sequence of keys from the dictionary.
#
#  Syntax :-   dict_name.keys()

person = {'name': 'Rahul', 'age':25, 'city': 'Delhi'}

keys = person.keys()
print(keys)              #  return dict_keys sequence
print(type(keys))

keysList = list(keys)    #  convert into list
print(keysList)















