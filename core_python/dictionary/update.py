#  Dictionary - update() Method
#
#  This method is used to update the dictionary with the specified key value pair.
# 
#  Syntax:-    dict_name.update(iterable)

person = {'name': 'Rahul', 'age':25, 'city': 'Delhi'}
print(person)

# update one key-value
person.update({'PIN': 123456})
print(person)
print()

# update multiple key-value
person.update({'name': 'Meera', 'city': 'Mumbai'})
print(person)


















