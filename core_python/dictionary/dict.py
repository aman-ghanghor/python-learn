# Dictionary are
# 1. Changeable
# 2. Items can be Accesssed by key
# 3. No duplicate


person = {'name': 'Rahul', 'age': 23, 'city': 'Delhi'}

print(person)
print(person['city'])

person['city'] = "Mumbai"
print(person)
print(len(person))
print("")

person['PIN'] = '123456'
print(person)






# Note - As of Python version 3.7, dictionaries are ordered. In Python 3.6 and earlier, dictionaries are unordered.










