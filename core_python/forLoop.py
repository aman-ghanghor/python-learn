# A for loop is used for iterating over a sequence (that is either a list, a tuple, a dictionary, 
# a set, or a string).

fruits = ["banana", "orange", "apple", "cherry"]
name = "Radha"

# Looging through collection
for x in fruits:
    if(x=='orange'):
        continue
    print(x)


# Looping through string
for x in name:
    print(x)








