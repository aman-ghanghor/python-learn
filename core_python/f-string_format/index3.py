# ------------------------- Floating ---------------------------

#            :[fill][align][sign][#][0][width][,][.precision]type

num = 12.56
num1 = -12.56

print( f"{num:9f}" )

print( f"{num:9.2f}" )                #  2 digit after decimal point

print( f"{num:+9.2f}" )              

print( f"{num1:9.2f}" )              

print( f"{num:09.2f}" )            

print( f"{num:<09.2f}" )             

print( f"{num:>09.2f}" )              

print( f"{num:^09.2f}" )             

print( f"{num:*>9.2f}" )              

print( f"{num:*<9.2f}" ) 

print( f"{num:*^9.2f}" ) 











































